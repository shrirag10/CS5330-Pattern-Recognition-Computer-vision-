import os
import json
import numpy as np

def generate_latex_results(results_dir, report_dir):
    """
    Parses summary_metrics.json and test_evaluation_metrics.json, and writes
    a LaTeX file report/results_summary.tex containing macro definitions
    for all experimental results to automate report compilation.
    """
    os.makedirs(report_dir, exist_ok=True)
    
    summary_path = os.path.join(results_dir, "summary_metrics.json")
    test_path = os.path.join(results_dir, "test_evaluation_metrics.json")
    
    if not os.path.exists(summary_path) or not os.path.exists(test_path):
        print("Warning: JSON results files not found. Cannot generate LaTeX summary.")
        return
        
    with open(summary_path, 'r') as f:
        summary_data = json.load(f)
    with open(test_path, 'r') as f:
        test_data = json.load(f)
        
    conditions = ['pretrained-frozen', 'random-frozen', 'random-full']
    classes = ['buildings', 'forest', 'glacier', 'mountain', 'sea', 'street']
    
    latex_lines = [
        "%% Automated results summary generated by generate_latex_results.py",
        "%% Do not edit manually!\n"
    ]
    
    for cond in conditions:
        cond_clean = cond.replace("-", "")  # e.g., pretrainedfrozen
        
        # --- 1. Validation Statistics ---
        epochs_list = []
        val_acc_list = []
        for seed, metrics in summary_data[cond].items():
            epochs_list.append(metrics['epochs_trained'])
            val_acc_list.append(metrics['best_val_acc'])
            
        mean_val_epochs = np.mean(epochs_list)
        std_val_epochs = np.std(epochs_list)
        mean_val_acc = np.mean(val_acc_list) * 100
        std_val_acc = np.std(val_acc_list) * 100
        
        latex_lines.append(f"\\newcommand{{\\{cond_clean}ValEpochsMean}}{{{mean_val_epochs:.1f}}}")
        latex_lines.append(f"\\newcommand{{\\{cond_clean}ValEpochsStd}}{{{std_val_epochs:.1f}}}")
        latex_lines.append(f"\\newcommand{{\\{cond_clean}ValAccMean}}{{{mean_val_acc:.2f}\\%}}")
        latex_lines.append(f"\\newcommand{{\\{cond_clean}ValAccStd}}{{{std_val_acc:.2f}\\%}}")
        
        # --- 2. Test Statistics ---
        mean_test_acc = test_data[cond]['summary']['mean_accuracy'] * 100
        std_test_acc = test_data[cond]['summary']['std_accuracy'] * 100
        closest_seed = test_data[cond]['summary']['closest_seed']
        
        latex_lines.append(f"\\newcommand{{\\{cond_clean}TestAccMean}}{{{mean_test_acc:.2f}\\%}}")
        latex_lines.append(f"\\newcommand{{\\{cond_clean}TestAccStd}}{{{std_test_acc:.2f}\\%}}")
        latex_lines.append(f"\\newcommand{{\\{cond_clean}ClosestSeed}}{{{closest_seed}}}")
        
        # --- 3. Representative Seed Metrics (precision, recall, f1) ---
        rep_data = test_data[cond][str(closest_seed)]
        for i, cls_name in enumerate(classes):
            p = rep_data['precision'][i] * 100
            r = rep_data['recall'][i] * 100
            f1 = rep_data['f1'][i] * 100
            
            latex_lines.append(f"\\newcommand{{\\{cond_clean}RepPrec{cls_name.capitalize()}}}{{{p:.1f}\\%}}")
            latex_lines.append(f"\\newcommand{{\\{cond_clean}RepRec{cls_name.capitalize()}}}{{{r:.1f}\\%}}")
            latex_lines.append(f"\\newcommand{{\\{cond_clean}RepF{cls_name.capitalize()}}}{{{f1:.1f}\\%}}")
            
        # Overall representative seed accuracy
        rep_acc = rep_data['accuracy'] * 100
        latex_lines.append(f"\\newcommand{{\\{cond_clean}RepAcc}}{{{rep_acc:.2f}\\%}}")
        
        # --- 4. Confusion Matrix for Representative Seed ---
        cm = rep_data['confusion_matrix']
        cm_macro = [f"\\newcommand{{\\{cond_clean}CM}}[2]{{%"]
        for r in range(len(classes)):
            for c in range(len(classes)):
                cm_macro.append(f"  \\ifnum##1={r}\\ifnum##2={c}{cm[r][c]}\\fi\\fi%")
        cm_macro.append("}")
        latex_lines.append("\n".join(cm_macro))
                
    output_file = os.path.join(report_dir, "results_summary.tex")
    with open(output_file, 'w') as f:
        f.write("\n".join(latex_lines) + "\n")
        
    print(f"Generated LaTeX results file: {output_file}")

